import re

STOPWORDS: frozenset[str] = frozenset({
    # русские предлоги/союзы (нормализованы: lower, ё→е)
    "для", "при", "или", "что", "это", "как", "его", "ее",
    "из", "по", "на", "от", "до", "за", "без", "под", "над",
    "про", "через", "между", "около", "вдоль", "после",
    # английские
    "the", "and", "for", "with", "not", "are", "was", "were",
})

UNITS_EXACT: frozenset[str] = frozenset({
    # масса
    "кг", "г", "мг", "мкг", "нг",
    # объём
    "л", "мл",
    # длина
    "мм", "см", "м", "рт.ст",
    # время
    "мс", "мсек", "сек", "мин", "ч",
    # прочие
    "%", "уд/мин",
    # лаб. единицы
    "ммоль/л", "мкмоль/л", "нг/мл", "пг/мл",
    "ед/л", "ме/мл", "мме/мл", "мкме/мл",
    # англ. и комбинации
    "mg", "ml", "kg", "cm", "mm",
    "mg/ml", "ml/kg", "ng/ml", "pg/ml",
})

# "мм рт. ст." разбивается tokenizer'ом на ["мм", "рт", "ст"]:
# "мм" → UNITS_EXACT, "рт"/"ст" → len=2 < 3 → все три дропаются без отдельного паттерна.
UNITS_REGEX: list[re.Pattern[str]] = [
    re.compile(r"^(мк)?ме/мл$"),
    re.compile(r"^(мк)?моль/л$"),
    re.compile(r"^(пг|нг|мг|мкг)/мл$"),
    re.compile(r"^ед/л$"),
]

ALIAS_MAP: dict[str, list[str]] = {
    # эндоскопия
    "фгдс":  ["эгдс"],
    "эгдс":  ["фгдс"],
    # кардиология
    "экг":   ["электрокардиография"],
    "эхокг": ["эхокардиография"],
    # биохимия
    "лпнп":  ["ldl"],
    "лпвп":  ["hdl"],
    "ldl":   ["лпнп"],
    "hdl":   ["лпвп"],
    # гормоны
    "ттг":   ["tsh"],
    "птг":   ["pth"],
    "tsh":   ["ттг"],
    "pth":   ["птг"],
    # онко-маркер
    "psa":   ["пса"],
    "пса":   ["psa"],
}


def is_unit(token: str) -> bool:
    if token in UNITS_EXACT:
        return True
    return any(p.match(token) for p in UNITS_REGEX)


def is_stopword(token: str) -> bool:
    return token in STOPWORDS
